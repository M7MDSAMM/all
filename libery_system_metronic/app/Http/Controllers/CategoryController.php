     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //

        $request->validate([
            'category_name'=>'required|string|min:3|max:20' ,
            'category_info'=>'required|string|min:3|max:50',
            'is_visible'=>'in:on|string'
        ]);

        $category= new Category();
        $category->category_name= $request->get('category_name');
        $category->category_info= $request->get('category_info');
        $category->is_visible= $request->has('is_visible');
        $is_saved = $category->save();

        if($is_saved){
            return redirect()->route('categories.index');
        }else{

        }
    }

